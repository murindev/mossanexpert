import Vue from 'vue'
declare module 'vue/types/vue' {
    interface Vue {
        $checkEmail(str:string): boolean
    }
}
Vue.prototype.$checkEmail = (str: string) => {
    const re = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
    return re.test(String(str).toLowerCase());
};
